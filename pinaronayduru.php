             <td width="656">Renk Körlüğü Tesbiti
Renk körlüğü; kırmızı, yeşil ve mavi renklerden bir ya da birden fazlasını ayırt edememeyle ortaya çıkan bozukluktur.  Kalıtsal renk körlüğünün tedavisi olmamakla birlikte, özel olarak renklendirilmiş gözlükler veya kontak lenslerle hastaların bazı renkleri ayır etmeleri sağlanabilmektedir. Renk körlüğü aynı zamanda bazı göz hastalıklarının sonucunda da ortaya çıkabilir. Bu durumda ise renk körlüğünün erken tesbiti önemlidir. <br>
Yetişkinler için renk körlüğü testinde kullanılan rakam testlerinin ( http://bit.ly/1Uwr43r )okul öncesi çocuklara uygulanması doğru sonuçlar vermeyebilir. Bu sorunun çözümü için, okul öncesi çocukların oynayabileceği, renk körü olan çocukları olmayanlardan ayırt edebilecek Android platformunda bir oyun tasarlanması beklenmektedir. <br>
Bu proje için öncelikle iyi bir araştırma yapılması, alanında uzman kişilerden de görüşler alınması gerekmektedir. 
Mevcutta kullanılan rakamsal görüntülerin detaylı olarak incelenmesi, tersine mühendislik yapılarak, benzer görüntülerin  nesneler(ev, araba vb) üzerine dinamik bir şekilde uygulanabilmesi için gerekli algoritma ve filitre tasarımının yapılması gerekmektedir. Öyle bir görüntü oluştumalısınız ki, renk körü olan kişiler görüntüde farklı bir nesne görürken, renk körü olmayan kişiler farklı bir nesne görebilmeli (İnternette böyle örnekler mevcut). Görüntüler dinamik bir şekilde oluşturulmalı.
Tüm bu bilgiler elde edildikten sonra örneğin renk körü olan çocukların doğru, renk körü olmayan çocukların yanlış oynayacağı bir oyun tasarımı yapılabilir. Pac-Man benzeri bir oyun olabilir. (Oyun senaryosu renk körü olan kişilerin yanlış oynayacağı şekilde tersine de çevrilebilir.) <br>
Ayrıca renk körlüğünün tek bir çeşit olmadığı, çeşitlerinin olduğu da dikkate alınmalıdır (Protonopıa, Trıtanopıa, Deuteranopıa, Anopia ).
Tüm geliştirilen uygulamalar gerçek renk körü olan kişiler ile irtibata geçilerek test edilemelidir. Bu hastalığa erkeklerde %8, kadınlarda ise %0.5 oranında rastlanmaktadır. <br><br>
